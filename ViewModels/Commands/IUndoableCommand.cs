using System;

namespace calcaot.ViewModels.Commands
{
    /// <summary>
    /// Интерфейс обратимой команды — паттерн Command (Команда).
    ///
    /// Паттерн Command инкапсулирует действие в виде отдельного объекта.
    /// Это позволяет:
    ///   • передавать команды как параметры;
    ///   • ставить их в очередь или записывать в историю;
    ///   • отменять выполненные действия (Undo).
    ///
    /// Каждая операция над едой (добавить, удалить, изменить) реализована
    /// как отдельный класс, реализующий этот интерфейс.
    /// Метод Execute() выполняет действие, Undo() — отменяет его.
    /// </summary>
    public interface IUndoableCommand
    {
        void Execute();
        void Undo();
    }
}
