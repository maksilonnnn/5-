using System;

namespace calcaot.Interfaces
{
    /// <summary>
    /// Паттерн Command (Команда): интерфейс обратимой команды с поддержкой отмены.
    ///
    /// Command инкапсулирует действие в отдельном объекте, чтобы:
    ///   • передавать команды как параметры;
    ///   • хранить их в истории/очереди;
    ///   • отменять выполненные действия (Undo).
    ///
    /// Методы:
    ///   Execute() — выполняет действие,
    ///   Undo()    — отменяет действие и возвращает состояние назад.
    /// </summary>
    public interface IUndoableCommand
    {
        void Execute();
        void Undo();
    }
}
